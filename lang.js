      const button = document.getElementById('lang-toggle');
      const getLang = () => localStorage.getItem('amelo_lang') || 'es';
      const applyTranslations = (lang) => {
        // document title
        const metaTitle = translations.meta && translations.meta.title ? translations.meta.title : (translations[lang] && translations[lang]['brand.name']) || document.title;
        document.title = metaTitle;

        // elements with data-i18n
        document.querySelectorAll('[data-i18n]').forEach(el=>{
          const key = el.getAttribute('data-i18n');
          if(!key) return;
          const val = (translations[lang] && translations[lang][key]) || '';
          // inputs of type submit/button -> set value
          if(el.tagName === 'INPUT' && (el.type === 'submit' || el.type === 'button')) {
            el.value = val;
            return;
          }
          // other elements -> innerHTML
          el.innerHTML = val;
        });

        // attributes mapping: data-i18n-attr="attr:key;attr2:key2"
        document.querySelectorAll('[data-i18n-attr]').forEach(el=>{
          const map = el.getAttribute('data-i18n-attr');
          if(!map) return;
          map.split(';').forEach(pair=>{
            const [attr,key] = pair.split(':').map(s=>s && s.trim());
            if(!attr || !key) return;
            const val = (translations[lang] && translations[lang][key]) || '';
            el.setAttribute(attr, val);
          });
        });

        // button text shows target language to switch to
        if(button) button.textContent = (lang === 'es') ? 'EN' : 'ES';
      };

      const setLang = (lang) => {
        localStorage.setItem('amelo_lang', lang);
        applyTranslations(lang);
      };

      if(button) {
        button.addEventListener('click', ()=> {
          const current = getLang();
          const next = current === 'es' ? 'en' : 'es';
          setLang(next);
        });
      }

      // nuevo: sincronizar apariencia del botón de idioma con el scroll
